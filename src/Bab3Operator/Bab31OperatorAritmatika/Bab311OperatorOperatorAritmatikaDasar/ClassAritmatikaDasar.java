package Bab3Operator.Bab31OperatorAritmatika.Bab311OperatorOperatorAritmatikaDasar;

public class ClassAritmatikaDasar {

	public void operasiAritmatika(int a, int b, int c) {
		System.out.println("===Operasi Aritmatika pada tipe integer===");
		System.out.println("Nilai a : " + a);
		System.out.println("Nilai b : " + b);
		System.out.println("Nilai c : " + c);
		System.out.println();
		System.out.println("Nilai penambahan a + b : " + (a + b));
		System.out.println("Nilai pengurangan a - b : " + (a - b));
		System.out.println("Nilai perkalian a * a : " + (a * a));
		System.out.println("Nilai pembagian a / b : " + (a / b));
		System.out.println("Nilai negasi b : " + (-b));
	}
}
