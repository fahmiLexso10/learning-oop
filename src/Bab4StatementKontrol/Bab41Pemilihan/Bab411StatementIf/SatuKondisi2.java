public class SatuKondisi2 {
	public static void main(String[] args) {
		
		char ch = 'E';
		
		if ((ch == 'a')||(ch == 'A')||(ch == 'i')||
				(ch == 'I')||(ch == 'u')||(ch == 'U')||
				(ch == 'e')||(ch == 'E')||(ch == 'o')||(ch == 'O')) {
			System.out.println(ch + " adalah huruf vokal");
		}
	}
}
